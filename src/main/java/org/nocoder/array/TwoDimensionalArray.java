package org.nocoder.array;

/**
 * 二维数组
 * @author yangjinlong
 */
public class TwoDimensionalArray {
    public static void main(String[] args) {
        Object[][] pairs = new Object[3][2];
        pairs[0] = new Object[]{"a", 1};
        pairs[1] = new Object[]{"b", 2};
        pairs[2] = new Object[]{"c", 3};
    }
}
